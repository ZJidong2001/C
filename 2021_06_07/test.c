#define NUM 100
#define MAX(X,Y)  (X>Y?X:Y)

int main()  // #define定义常量和宏
{
	printf("%d\n", NUM);

	int a = 10;
	int b = 20;
	int c = MAX(a + 2, b);
	printf("%d\n", c);
	return 0;
}