// 整数在内存中存储的时候，存储的是二进制
// 一个整数的二进制有3种表示形式：原码、反码、补码
// 内存中存储整数的时候，存储的是二进制的补码，打印的是原码，计算时采用的也是补码

// 正整数：原码、反码、补码相同
// 负整数：原码、反码、补码是需要计算的
// 有符号的整数：最高位是0，表示正整数；最高位是1，表示负整数

